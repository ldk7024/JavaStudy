
public class ex01 {

	public static void main(String[] args) {
		// 총합
		int sum = 0;
		// 총합에 더해줄 수
		int num = 1;

		while (true) {
			sum += num; // 누적
			if (sum >= 1000) {
				break;
			
			
			
			}
		num++;
		
		
		}
		System.out.printf("1부터 %d까지 총합: %d", num, sum);

	}

}
