
public class address {
private String name;
private int age;
private String phonenumber;
public String getName() {
	return name;
}
public int getAge() {
	return age;
}
public String getPhonenumber() {
	return phonenumber;
}
public address(String name, int age, String phonenumber) {
	super();
	this.name = name;
	this.age = age;
	this.phonenumber = phonenumber;
}
}
